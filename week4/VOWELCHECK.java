package week4;

public class VOWELCHECK{
    public static void main(String[] args){
        char c='z';
        if(c=='a'||c=='e'||c=='i'||c=='o'||c=='u'){
            System.out.print("'"+c+"' is a vowel.");
        }
        else{
            System.out.print("'"+c+"' is a consonant.");
        }
    }
}
